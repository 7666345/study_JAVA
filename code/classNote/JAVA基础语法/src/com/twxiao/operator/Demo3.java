package com.twxiao.operator;

public class Demo3 {
    public static void main(String[] args) {

        int a=10;
        int b=20;

        //关系运算，是比较，返回的结果只有是或否，为布尔值。

        System.out.println(a>b); //大于
        System.out.println(a<b); //小于
        System.out.println(a==b); //等于
        System.out.println(a!=b); //不等于
    }
}
